import React, { useState, useEffect, useCallback } from 'react';
import {
  StyleSheet,
  View,
  Text,
  RefreshControl,
  Alert,
  SafeAreaView,
  ScrollView,
  TouchableOpacity
} from 'react-native';
import MaterialIcons from 'react-native-vector-icons/MaterialIcons';
import { apiService } from '../../services/api';
import Loading from '../../components/Loading';
import AppCard from '../../components/AppCard';
import { useTheme } from '../../theme';

export default function SalesReportScreen() {
  const { colors } = useTheme();
  const styles = createStyles(colors);
  const [report, setReport] = useState(null);
  const [reportType, setReportType] = useState('DAILY'); // DAILY, WEEKLY, MONTHLY
  const [loading, setLoading] = useState(true);
  const [refreshing, setRefreshing] = useState(false);

  const fetchReport = useCallback(async () => {
    try {
      const todayStr = new Date().toISOString().split('T')[0];
      let data;
      if (reportType === 'DAILY') {
        data = await apiService.sales.getDailyReport(todayStr);
      } else if (reportType === 'WEEKLY') {
        data = await apiService.sales.getWeeklyReport(todayStr);
      } else {
        data = await apiService.sales.getMonthlyReport(todayStr);
      }
      setReport(data);
    } catch (e) {
      console.error(e);
      Alert.alert('Report Error', 'Unable to retrieve sales report details.');
    } finally {
      setLoading(false);
      setRefreshing(false);
    }
  }, [reportType]);

  useEffect(() => {
    setLoading(true);
    fetchReport();
  }, [fetchReport]);

  const onRefresh = () => {
    setRefreshing(true);
    fetchReport();
  };

  if (loading && !refreshing) {
    return <Loading message="Generating report sheets..." fullScreen />;
  }

  return (
    <SafeAreaView style={styles.container}>
      {/* Tabs */}
      <View style={styles.tabBar}>
        <TouchableOpacity
          style={[styles.tab, reportType === 'DAILY' && styles.tabActive]}
          onPress={() => setReportType('DAILY')}
        >
          <Text style={[styles.tabText, reportType === 'DAILY' && styles.tabTextActive]}>Daily</Text>
        </TouchableOpacity>
        <TouchableOpacity
          style={[styles.tab, reportType === 'WEEKLY' && styles.tabActive]}
          onPress={() => setReportType('WEEKLY')}
        >
          <Text style={[styles.tabText, reportType === 'WEEKLY' && styles.tabTextActive]}>Weekly</Text>
        </TouchableOpacity>
        <TouchableOpacity
          style={[styles.tab, reportType === 'MONTHLY' && styles.tabActive]}
          onPress={() => setReportType('MONTHLY')}
        >
          <Text style={[styles.tabText, reportType === 'MONTHLY' && styles.tabTextActive]}>Monthly</Text>
        </TouchableOpacity>
      </View>

      <ScrollView
        contentContainerStyle={styles.scrollContent}
        refreshControl={
          <RefreshControl refreshing={refreshing} onRefresh={onRefresh} colors={[colors.primary]} tintColor={colors.primary} />
        }
      >
        {/* Report Document Header */}
        <AppCard style={styles.reportHeaderCard}>
          <Text style={styles.reportTitle}>{report?.title || 'DAWN BREAD - SALES REPORT'}</Text>
          <Text style={styles.reportRange}>Period: {report?.dateRange || 'N/A'}</Text>
          <Text style={styles.reportMeta}>Generated By: {report?.generatedBy || 'N/A'} | At: {report?.generatedAt ? `${new Date(report.generatedAt).toLocaleDateString()} ${new Date(report.generatedAt).toLocaleTimeString()}` : 'N/A'}</Text>
          
          <View style={styles.divider} />
          
          <View style={styles.statsSummaryRow}>
            <View style={styles.statsCell}>
              <Text style={styles.statsValue}>PKR {(report?.totalRevenue || 0).toLocaleString()}</Text>
              <Text style={styles.statsLabel}>Total Revenue</Text>
            </View>
            <View style={styles.statsCell}>
              <Text style={styles.statsValue}>{report?.totalUnits || 0}</Text>
              <Text style={styles.statsLabel}>Total Units Sold</Text>
            </View>
            <View style={styles.statsCell}>
              <Text style={styles.statsValue}>{report?.activeAgents || 0}</Text>
              <Text style={styles.statsLabel}>Agents Active</Text>
            </View>
          </View>
        </AppCard>

        {/* Detailed Agent Breakdowns */}
        {reportType === 'DAILY' && (
          <>
            <Text style={styles.sectionTitle}>Agent Dispatches</Text>
            {report?.agentSummaries && report.agentSummaries.length > 0 ? (
              report.agentSummaries.map((summary, idx) => (
                <AppCard key={idx} style={styles.agentCard}>
                  <View style={styles.agentHeader}>
                    <MaterialIcons name="person" size={20} color={colors.secondary} />
                    <Text style={styles.agentName}>{summary.agentName} ({summary.employeeId})</Text>
                  </View>
                  <View style={styles.table}>
                    <View style={styles.tableHeaderRow}>
                      <Text style={[styles.tableHeaderCell, { flex: 2 }]}>Product</Text>
                      <Text style={[styles.tableHeaderCell, { flex: 0.6, textAlign: 'center' }]}>Qty</Text>
                      <Text style={[styles.tableHeaderCell, { flex: 1.2, textAlign: 'right' }]}>Unit Price</Text>
                      <Text style={[styles.tableHeaderCell, { flex: 1.2, textAlign: 'right' }]}>Total</Text>
                    </View>
                    {summary.items.map((item, itemIdx) => (
                      <View key={itemIdx} style={styles.tableBodyRow}>
                        <Text style={[styles.tableCell, { flex: 2, fontWeight: '500' }]} numberOfLines={1}>
                          {item.productName}
                        </Text>
                        <Text style={[styles.tableCell, { flex: 0.6, textAlign: 'center', fontWeight: 'bold' }]}>
                          {item.quantity}
                        </Text>
                        <Text style={[styles.tableCell, { flex: 1.2, textAlign: 'right', color: colors.textSecondary }]}>
                          PKR {item.unitPrice}
                        </Text>
                        <Text style={[styles.tableCell, { flex: 1.2, textAlign: 'right', fontWeight: 'bold', color: colors.successDark }]}>
                          PKR {item.totalPrice}
                        </Text>
                      </View>
                    ))}
                  </View>
                  <View style={styles.agentFooterRow}>
                    <Text style={styles.agentFooterText}>Total Units: {summary.totalUnits}</Text>
                    <Text style={[styles.agentFooterText, { fontWeight: 'bold', color: colors.secondary }]}>
                      Total Revenue: PKR {(summary.totalRevenue || 0).toLocaleString()}
                    </Text>
                  </View>
                </AppCard>
              ))
            ) : (
              <AppCard style={styles.emptyCard}>
                <Text style={styles.emptyText}>No agent dispatches submitted today.</Text>
              </AppCard>
            )}
          </>
        )}

        {/* Product Performance ranking */}
        <View style={styles.sectionTitleRow}>
          <MaterialIcons name="trending-up" size={16} color={colors.textSecondary} />
          <Text style={styles.sectionTitleWithIcon}>Top Selling Products</Text>
        </View>
        <AppCard style={styles.rankingCard}>
          <View style={styles.tableHeaderRow}>
            <Text style={[styles.tableHeaderCell, { flex: 0.5 }]}>No</Text>
            <Text style={[styles.tableHeaderCell, { flex: 2 }]}>Product Name</Text>
            <Text style={[styles.tableHeaderCell, { flex: 1, textAlign: 'center' }]}>Units</Text>
            <Text style={[styles.tableHeaderCell, { flex: 1.5, textAlign: 'right' }]}>Revenue</Text>
          </View>
          {report?.productPerformance && report.productPerformance.length > 0 ? (
            report.productPerformance.map((p, idx) => (
              <View key={idx} style={styles.tableBodyRow}>
                <Text style={[styles.tableCell, { flex: 0.5, color: colors.textSecondary, fontWeight: 'bold' }]}>{idx + 1}</Text>
                <Text style={[styles.tableCell, { flex: 2, fontWeight: '600' }]} numberOfLines={1}>{p.productName}</Text>
                <Text style={[styles.tableCell, { flex: 1, textAlign: 'center', fontWeight: 'bold' }]}>{p.quantitySold}</Text>
                <Text style={[styles.tableCell, { flex: 1.5, textAlign: 'right', fontWeight: 'bold', color: colors.successDark }]}>
                  PKR {(p.totalRevenue || 0).toLocaleString()}
                </Text>
              </View>
            ))
          ) : (
            <Text style={styles.emptyText}>No product performance data compiled.</Text>
          )}
        </AppCard>
      </ScrollView>
    </SafeAreaView>
  );
}

